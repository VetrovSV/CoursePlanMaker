def verify():
    """Проверка после наполнения."""
    conn = create_db(DB_PATH)
    rows = conn.execute(
        """SELECT dip.semester, d.code, d.name,
                  dip.hours_total, dip.hours_lecture, dip.hours_lab,
                  dip.hours_practice, dip.hours_self_study, dip.hours_exam,
                  dip.credits, dip.exam_form
           FROM disciplines_in_plans dip
           JOIN disciplines d ON d.id = dip.discipline_id
           JOIN study_plans sp ON sp.id = dip.plan_id
           WHERE sp.year = 2025 AND sp.form = 'очная'
             AND sp.direction_id = (SELECT id FROM directions WHERE code = '09.03.01' AND level = 'бакалавриат')
           ORDER BY dip.semester, d.code"""
    ).fetchall()

    print("=" * 90)
    print(f"{'Сем':<4} {'Код':<18} {'Название':<55} {'Ч':<5} {'ЗЕТ':<5} {'Форма'}")
    print("=" * 90)
    total_hours = 0
    total_credits = 0
    for r in rows:
        sem, code, name, h_total, *_ = r
        credits = r[9]
        exam_form = r[10] or "ВКР"
        name_short = name[:52] + "..." if len(name) > 55 else name
        print(f"{sem:<4} {code or '':<18} {name_short:<55} {h_total:<5} {credits:<5} {exam_form}")
        total_hours += h_total
        total_credits += credits

    print("=" * 90)
    print(f"{'ИТОГО:':<22} {'':<18} {'':<55} {total_hours:<5} {total_credits:<5}")

    # Проверка сходимости с PDF: Блок 1 (7892) + Блок 2 (756) + Блок 3 (324) = 8972
    print(f"\nОжидаемая общая трудоёмкость по PDF: 8972 ч")
    print(f"Получено в БД: {total_hours} ч")
    print(f"Разница: {total_hours - 8972} ч")

    conn.close()


